function Zo = MW_DiskWarp(R, theta)
    
    W0 =  2.063e-4 * R.^3 - 0.002329 * R.^2 + 0.001374 * R - 0.03613;
    W1 = -6.838e-5 * R.^3 - 0.005807 * R.^2 + 0.063540 * R - 0.11340;
    W2 =  1.217e-4 * R.^3 + 0.001202 * R.^2 - 0.058400 * R + 0.26010;
    
    Zo = W0 + W1 * sin(theta - 1.47548) + W2 * sin(2*theta - 2.76487);